    private void gravarTMB(double tmb) {
    try (Connection connection = conexao.getConnection()) {
        String query = "INSERT INTO tmb_data (tmb, data, horario) VALUES (?, CURDATE(), CURTIME())";
        PreparedStatement statement = connection.prepareStatement(query);
        statement.setDouble(1, tmb);
        statement.executeUpdate();
        JOptionPane.showMessageDialog(this, "TMB gravado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        // Limpar os campos de texto
        txt_pesoTmb.setText("");
        txt_alturaTmb.setText("");
        txt_idadeTmb.setText("");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro ao gravar TMB: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
}


private void verUltimos5TMBs() {
    try (Connection connection = conexao.getConnection()) {
        String query = "SELECT * FROM tmb_data ORDER BY id DESC LIMIT 5";
        PreparedStatement statement = connection.prepareStatement(query);
        ResultSet resultSet = statement.executeQuery();
        StringBuilder sb = new StringBuilder();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        while (resultSet.next()) {
            double tmb = resultSet.getDouble("tmb");
            Date data = resultSet.getDate("data");
            Time horario = resultSet.getTime("horario");
            String dataFormatada = dateFormat.format(data);
            sb.append("TMB: ").append(tmb).append(", Data: ").append(dataFormatada).append(", Horário: ").append(horario).append("\n");
        }
        JOptionPane.showMessageDialog(this, sb.toString(), "Últimos 5 TMBs", JOptionPane.INFORMATION_MESSAGE);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro ao buscar os últimos 5 TMBs: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
}
    
