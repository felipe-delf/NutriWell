    private void btn_calcularTmbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularTmbActionPerformed
        // Obter os valores dos campos de entrada
        double peso = Double.parseDouble(txt_pesoTmb.getText());
        double altura = Double.parseDouble(txt_alturaTmb.getText());
        int idade = Integer.parseInt(txt_idadeTmb.getText());
        
        // Calcular a TMB de acordo com o sexo
        double tmb;
        if (rb_masculino.isSelected()) {
            tmb = 66.5 + (13.75 * peso) + (5 * altura) - (6.75 * idade);
        } else {
            tmb = 655.1 + (9.563 * peso) + (1.85 * altura) - (4.676 * idade);
        }
        
        // Aplicar o fator de atividade
        double fatorAtividade = 1.0;
        if (rb_sedentario.isSelected()) {
            fatorAtividade = 1.2;
        } else if (rb_leve.isSelected()) {
            fatorAtividade = 1.375;
        } else if (rb_moderado.isSelected()) {
            fatorAtividade = 1.55;
        } else if (rb_pesado.isSelected()) {
            fatorAtividade = 1.725;
        }
        
        // Calcular a TMB com o fator de atividade
        double tmbComAtividade = tmb * fatorAtividade;
        
        // Formatar o resultado com dois números após a vírgula
        String resultadoFormatado = decimalFormat.format(tmbComAtividade);
        
        // Exibir o resultado
        txt_resultadoTmb.setText("Seu TMB é de " + resultadoFormatado + " Kcal");
    }//GEN-LAST:event_btn_calcularTmbActionPerformed

